#include <stdio.h>

int main ()
{
	int first_number = 10;
	int second_number = 60;

	// addition
	printf("%d\n", first_number+second_number);
	
	// Positive subtraction
	printf("%d\n", second_number - first_number);

	// Negative subtraction
	printf("%d\n", first_number - second_number);

	// Multiplication
	printf("%d\n", first_number * second_number);

	// division
	printf("%d\n", second_number / first_number );

	return 0;
}
